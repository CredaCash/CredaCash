/*

Contains encoding tables:

	baseN table comverts numbers mod N --> an alphabet with N characters
	baseNrev table does the reverse (alphabet of N characters --> numbers mod N)

The rev tables can also be used to reversibly compress a string that uses only the fixed alphabet.

	baseNcomborev combines two rev tables, which might for example contain lower and upper case letters that convert to the same numbers

The baseNcomborev tables cannot be used for compression, because they are not reversible.

base256 = all characters
base224 = ascii extended without control chars = chars 32-255
base95 = ascii without control chars = chars 32-126
base87 = Adobe ASCII-85 and btoa charset = chars 33 to u, plus y and z
base85 = RFC-1924 charset = chars 33 to 126, except "',./:[\]
base66 = the characters in base64 and base64url
base64 = RFC-4648 standard base64 = mixedcase alphanums plus '+' and '/'
base64url = RFC-4648 standard base64url for encoding to url friendly chars = mixedcase alphanums plus '-' and '_'
base58 = bitcoin standard base58 = mixedcase alphanums except '0', 'I', 'O', and 'l'
base57 = CredaCash standard -- base58 without the digit '1', and in a different order
base38 and base38uc = digits and either uppercase or lowercase latin letters plus '-' and '_'
base36 and base36uc = digits and either uppercase or lowercase latin letters
base34 = bech32 seqwit character set = lowercase alphanums except 'i' and 'o'
base32 = RFC-4648 standard base32 = A-Z then 2-7
base32z = zbase character set = lowercase alphanums except '0', 'l', 'v' and '2'
base26 and base26uc = lowercase or uppercase latin letters
base17 and base17uc = lowercase or uppercase hexadecimal digits plus 'x' or 'X'
base16 and base16uc = lowercase or uppercase hexadecimal digits
base10 = base 10 digits
base8 = digits 2-9 = digits except 0 and 1

*/

